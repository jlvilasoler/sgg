export const CHAT_RECENT_EMOJIS_KEY = "scg-chat-recent-emojis";
export const CHAT_RECENT_EMOJIS_MAX = 28;

export function loadRecentEmojis(): string[] {
  try {
    const raw = sessionStorage.getItem(CHAT_RECENT_EMOJIS_KEY);
    if (!raw) return [];
    const parsed = JSON.parse(raw) as unknown;
    if (!Array.isArray(parsed)) return [];
    return parsed.filter((e): e is string => typeof e === "string").slice(0, CHAT_RECENT_EMOJIS_MAX);
  } catch {
    return [];
  }
}

export function saveRecentEmoji(emoji: string): string[] {
  const prev = loadRecentEmojis().filter((e) => e !== emoji);
  const next = [emoji, ...prev].slice(0, CHAT_RECENT_EMOJIS_MAX);
  try {
    sessionStorage.setItem(CHAT_RECENT_EMOJIS_KEY, JSON.stringify(next));
  } catch {
    /* ignorar */
  }
  return next;
}
